// Función para inicializar el dropdown de idiomas
function initializeLanguageDropdown() {
    const dropdown = document.querySelector('.language-selector .dropdown-toggle');
    if (dropdown) {
        // Inicializar el dropdown solo una vez
        if (!dropdown._dropdown) {
            dropdown._dropdown = new bootstrap.Dropdown(dropdown);
        }
    }
}

// Función para cerrar el dropdown de idiomas
function closeLanguageDropdown() {
    const dropdown = document.querySelector('.language-selector .dropdown-toggle');
    if (dropdown && dropdown._dropdown) {
        dropdown._dropdown.hide();
    }
}



// Función para cambiar el idioma
function switchLanguage(lang) {
    currentLanguage = lang;
    updateTranslations();
    closeLanguageDropdown();
}

function updateTranslations() {
    // Actualizar el menú
    const homeLink = document.querySelector('.home-link');
    if (homeLink) homeLink.textContent = translations[currentLanguage].home;
    
    const aboutLink = document.querySelector('.about-link');
    if (aboutLink) aboutLink.textContent = translations[currentLanguage].about;
    
    const portfolioLink = document.querySelector('.portfolio-link');
    if (portfolioLink) portfolioLink.textContent = translations[currentLanguage].portfolio;
    
    const contactLink = document.querySelector('.contact-link');
    if (contactLink) contactLink.textContent = translations[currentLanguage].contact;
    
    // Actualizar los elementos del dropdown
    document.querySelectorAll('.nav-menu .dropdown-menu a').forEach(link => {
        const key = link.getAttribute('data-key');
        if (key && translations[currentLanguage][key]) {
            link.textContent = translations[currentLanguage][key];
        }
    });

    // Actualizar el contenido de home2.blade.php
    // Slide 1
    const activeSlide = document.querySelector('.carousel-item.active h2');
    if (activeSlide) {
        activeSlide.innerHTML = translations[currentLanguage].welcome_title;
        const activeP = document.querySelector('.carousel-item.active p');
        if (activeP) activeP.innerHTML = translations[currentLanguage].welcome_description;
        const activeBtn = document.querySelector('.carousel-item.active .btn-get-started');
        if (activeBtn) activeBtn.innerHTML = translations[currentLanguage].start_renovation;
    }

    // Slide 2
    const carouselItems = document.querySelectorAll('.carousel-item');
    if (carouselItems.length > 1) {
        const slide2H2 = carouselItems[1].querySelector('h2');
        if (slide2H2) {
            slide2H2.innerHTML = translations[currentLanguage].who_we_are;
            const slide2P = carouselItems[1].querySelector('p');
            if (slide2P) { slide2P.innerHTML = translations[currentLanguage].who_we_are_desc; }
            const slide2Btn = carouselItems[1].querySelector('.btn-get-started');
            if (slide2Btn) { slide2Btn.innerHTML = translations[currentLanguage].more_info; }
        }
    }

    // Slide 3
    if (carouselItems.length > 2) {
        const slide3H2 = carouselItems[2].querySelector('h2');
        if (slide3H2) {
            slide3H2.innerHTML = translations[currentLanguage].transformamos;
            const slide3P = carouselItems[2].querySelector('p');
            if (slide3P) { slide3P.innerHTML = translations[currentLanguage].professional_description; }
        }
    }
    
    // Seleccionar todos los elementos con data-key para traducción
    const elements = document.querySelectorAll('[data-key]');
    console.log('Elementos encontrados con data-key: ' + elements.length);
    
    elements.forEach(element => {
        const key = element.getAttribute('data-key');
        if (translations[currentLanguage][key]) {
            element.textContent = translations[currentLanguage][key];
            console.log(`Traducido: ${key} = ${translations[currentLanguage][key]}`);
        } else {
            console.warn(`No se encontró traducción para la clave: ${key} en el idioma: ${currentLanguage}`);
        }
    });

    const placeholderElements = document.querySelectorAll('[data-key-placeholder]');
    console.log('Elementos encontrados con data-key-placeholder: ' + placeholderElements.length);
    
    placeholderElements.forEach(element => {
        const key = element.getAttribute('data-key-placeholder');
        if (translations[currentLanguage][key]) {
            element.placeholder = translations[currentLanguage][key];
        } else {
            console.warn(`No se encontró traducción para el placeholder con clave: ${key} en el idioma: ${currentLanguage}`);
        }
    });
}


// Función para cerrar el dropdown de idiomas
function closeLanguageDropdown() {
    const dropdown = document.querySelector('.language-selector .dropdown-toggle');
    if (dropdown && dropdown._dropdown) {
        dropdown._dropdown.hide();
    }
}

function switchLanguage(lang) {
    currentLanguage = lang;
    updateTranslations();
    closeLanguageDropdown();
    
    // Guardar el idioma en localStorage
    localStorage.setItem('language', lang);
}

// Cargar el idioma guardado al inicio
const savedLanguage = localStorage.getItem('language');
if (savedLanguage) {
    currentLanguage = savedLanguage;
    updateTranslations();
}
